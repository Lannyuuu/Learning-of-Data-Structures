public class Arithmetic_Test {
    public static void main(String[] args) {
        // 正数相除:去掉小数部分
        int result1 = 7 / 2;
        System.out.println("7 / 2 的结果是: " + result1);

        // 负数相除：去掉小数部分
        int result2 = -7 / 2;
        System.out.println("-7 / 2 的结果是: " + result2);

        // 正数除以负数：去掉小数部分
        int result3 = 7 / -2;
        System.out.println("7 / -2 的结果是: " + result3);

        // 负数除以负数：去掉小数部分
        int result4 = -7 / -2;
        System.out.println("-7 / -2 的结果是: " + result4);
    }
}
